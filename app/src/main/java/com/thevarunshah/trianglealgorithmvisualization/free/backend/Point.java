package com.thevarunshah.trianglealgorithmvisualization.free.backend;

public class Point {

    private final float x;
    private final float y;

    public Point(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float getX(){
        return this.x;
    }

    public float getY() {
        return this.y;
    }
}
